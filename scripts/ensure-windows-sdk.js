/**
 * Detect the installed Windows 10 SDK and write Directory.Build.props so all
 * projects (including react-native-windows) target an SDK that is present.
 */
const fs = require('fs');
const path = require('path');

const kitsRoot = path.join(
  process.env['ProgramFiles(x86)'] || 'C:\\Program Files (x86)',
  'Windows Kits',
  '10',
);
const includeRoot = path.join(kitsRoot, 'Include');
const uapRoot = path.join(kitsRoot, 'Platforms', 'UAP');
const outputPath = path.join(__dirname, '..', 'Directory.Build.props');

function parseVersion(name) {
  const match = /^10\.0\.(\d+)\.0$/.exec(name);
  return match ? Number(match[1]) : 0;
}

function getLatestSdkVersion() {
  if (!fs.existsSync(includeRoot)) {
    return null;
  }

  const versions = fs
    .readdirSync(includeRoot, {withFileTypes: true})
    .filter(entry => entry.isDirectory() && entry.name.startsWith('10.0.'))
    .map(entry => entry.name)
    .filter(version => {
      const hasUap = fs.existsSync(path.join(uapRoot, version));
      return hasUap || !fs.existsSync(uapRoot);
    })
    .sort((a, b) => parseVersion(a) - parseVersion(b));

  return versions.length > 0 ? versions[versions.length - 1] : null;
}

const sdkVersion = getLatestSdkVersion();
if (!sdkVersion) {
  console.error(
    'No Windows 10 SDK found. Install the "Windows 10 SDK" workload in Visual Studio 2026.',
  );
  process.exit(1);
}

const contents = `<?xml version="1.0" encoding="utf-8"?>
<!-- Auto-generated by scripts/ensure-windows-sdk.js -->
<Project xmlns="http://schemas.microsoft.com/developer/msbuild/2003">
  <PropertyGroup>
    <WindowsTargetPlatformVersion>${sdkVersion}</WindowsTargetPlatformVersion>
    <TargetPlatformVersion>${sdkVersion}</TargetPlatformVersion>
  </PropertyGroup>
  <ItemDefinitionGroup>
    <ClCompile>
      <PreprocessorDefinitions>_SILENCE_EXPERIMENTAL_COROUTINE_DEPRECATION_WARNINGS;%(PreprocessorDefinitions)</PreprocessorDefinitions>
    </ClCompile>
  </ItemDefinitionGroup>
</Project>
`;

const existing = fs.existsSync(outputPath)
  ? fs.readFileSync(outputPath, 'utf8')
  : '';

if (existing !== contents) {
  fs.writeFileSync(outputPath, contents);
  console.log(`Using Windows SDK ${sdkVersion}`);
}
